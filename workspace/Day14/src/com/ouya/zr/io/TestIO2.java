package com.ouya.zr.io;

import java.io.File;

/**
 * 判断一个给定的路径是否是目录
 * isDirectory()
 * 
 * 列出指定目录的全部文件（包括子文件）
 * 提示：需要静态的通用方法
 * @author Administrator
 *
 */
public class TestIO2 {

	public static void main(String[] args) {
		
		File f = new File("e:"+File.separator);
		if (f.isDirectory()) {
			System.out.println(f.getPath()+"是目录");
		}else{
			System.out.println(f.getPath()+"不是目录");
		}
	}
}
